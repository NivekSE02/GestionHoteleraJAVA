package Modelos;

/**
 *
 * @author nivek
 */
public class Habitacion {
    private int habitacion_id;
    private int tipo_id;
    private String numero;
    private int piso;
    private boolean vista_mar;
    private String estado_actual;
    private String notas;

    public int getHabitacion_id() {
        return habitacion_id;
    }

    public void setHabitacion_id(int habitacion_id) {
        this.habitacion_id = habitacion_id;
    }

    public int getTipo_id() {
        return tipo_id;
    }

    public void setTipo_id(int tipo_id) {
        this.tipo_id = tipo_id;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

    public int getPiso() {
        return piso;
    }

    public void setPiso(int piso) {
        this.piso = piso;
    }

    public boolean isVista_mar() {
        return vista_mar;
    }

    public void setVista_mar(boolean vista_mar) {
        this.vista_mar = vista_mar;
    }

    public String getEstado_actual() {
        return estado_actual;
    }

    public void setEstado_actual(String estado_actual) {
        this.estado_actual = estado_actual;
    }

    public String getNotas() {
        return notas;
    }

    public void setNotas(String notas) {
        this.notas = notas;
    }
}
