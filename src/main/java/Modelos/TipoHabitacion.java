package Modelos;

/**
 *
 * @author nivek
 */
public class TipoHabitacion {
    private int tipo_id;
    private String nombre;
    private double precio_base_noche;
    private int capacidad_max;
    private String descripcion;
    private String comodidades;

    public int getTipo_id() {
        return tipo_id;
    }

    public void setTipo_id(int tipo_id) {
        this.tipo_id = tipo_id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getPrecio_base_noche() {
        return precio_base_noche;
    }

    public void setPrecio_base_noche(double precio_base_noche) {
        this.precio_base_noche = precio_base_noche;
    }

    public int getCapacidad_max() {
        return capacidad_max;
    }

    public void setCapacidad_max(int capacidad_max) {
        this.capacidad_max = capacidad_max;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getComodidades() {
        return comodidades;
    }

    public void setComodidades(String comodidades) {
        this.comodidades = comodidades;
    }
}
