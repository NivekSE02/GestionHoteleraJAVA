    public void listar() {
    modelo.setRowCount(0);

    String[] columnas = {
    "ID", "Huesped", "Empleado", "Habitación",
    "Entrada", "Salida", "Precio", "Estado",
    "Adultos", "Niños"
};
    modelo.setColumnIdentifiers(columnas);

    for (Reserva r : dao.listar()) {
        modelo.addRow(new Object[]{
            r.getReserva_id(),
            r.getHuesped_id(),
            r.getEmpleado_id(),
            r.getHabitacion_id(),
            r.getFecha_entrada(),
            r.getFecha_salida(),
            r.getPrecio_total(),
            r.getEstado()
        });
    }

    tablaReservas.setModel(modelo);
}
    

