    private void guardar() {
    Reserva r = new Reserva();

    r.setHuesped_id(Integer.parseInt(txtHuespedId.getText()));
    r.setEmpleado_id(Integer.parseInt(txtEmpleadoId.getText()));
    r.setHabitacion_id(Integer.parseInt(txtHabitacionId.getText()));
    r.setFecha_entrada(txtFechaEntrada.getText());
    r.setFecha_salida(txtFechaSalida.getText());
    r.setPrecio_total(Double.parseDouble(txtPrecio.getText()));
    r.setEstado(cbEstado.getSelectedItem().toString());
   
    r.setNumero_adultos(Integer.parseInt(txtAdultos.getText()));
    r.setNumero_ninos(Integer.parseInt(txtNinos.getText()));
    r.setObservaciones(txtObservaciones.getText());

    if (dao.insertar(r)) {
        listar();
        limpiar();
    }
}
    
    
    private void actualizar() {
    Reserva r = new Reserva();

    r.setReserva_id(Integer.parseInt(txtId.getText()));
    r.setHuesped_id(Integer.parseInt(txtHuespedId.getText()));
    r.setEmpleado_id(Integer.parseInt(txtEmpleadoId.getText()));
    r.setHabitacion_id(Integer.parseInt(txtHabitacionId.getText()));
    r.setFecha_entrada(txtFechaEntrada.getText());
    r.setFecha_salida(txtFechaSalida.getText());
    r.setPrecio_total(Double.parseDouble(txtPrecio.getText()));
    r.setEstado(cbEstado.getSelectedItem().toString());
   
    r.setNumero_adultos(Integer.parseInt(txtAdultos.getText()));
    r.setNumero_ninos(Integer.parseInt(txtNinos.getText()));
    r.setObservaciones(txtObservaciones.getText());

    if (dao.actualizar(r)) {
        listar();
        limpiar();
    }
}
    
    private void eliminar() {
    int id = Integer.parseInt(txtId.getText());

    if (dao.eliminar(id)) {
        listar();
        limpiar();
    }
}
    
   private void tablaClick() {
    int fila = tablaReservas.getSelectedRow();

    if (fila == -1) return;

    txtId.setText(modelo.getValueAt(fila, 0).toString());
    txtHuespedId.setText(modelo.getValueAt(fila, 1).toString());
    txtEmpleadoId.setText(modelo.getValueAt(fila, 2).toString());
    txtHabitacionId.setText(modelo.getValueAt(fila, 3).toString());
    txtFechaEntrada.setText(modelo.getValueAt(fila, 4).toString());
    txtFechaSalida.setText(modelo.getValueAt(fila, 5).toString());
    txtPrecio.setText(modelo.getValueAt(fila, 6).toString());
    cbEstado.setSelectedItem(modelo.getValueAt(fila, 7).toString());
}
    
